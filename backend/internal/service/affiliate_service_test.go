func TestResolveRebateRatePercentUsesGlobalRate(t *testing.T) {
	t.Parallel()
	svc := &AffiliateService{}

	require.InDelta(t, AffiliateRebateRateDefault,
		svc.resolveRebateRatePercent(context.Background(), &AffiliateSummary{}), 1e-9)

	for _, legacyRate := range []float64{0, 50, 250, -5, math.NaN()} {
		legacyRate := legacyRate
		require.InDelta(t, AffiliateRebateRateDefault,
			svc.resolveRebateRatePercent(context.Background(), &AffiliateSummary{AffRebateRatePercent: &legacyRate}), 1e-9)
	}
}

